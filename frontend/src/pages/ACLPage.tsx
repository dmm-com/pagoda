import { zodResolver } from "@hookform/resolvers/zod";
import LockIcon from "@mui/icons-material/Lock";
import { Box, Container, Typography } from "@mui/material";
import { useSnackbar } from "notistack";
import React, { FC, useCallback, useState, useEffect } from "react";
import { useForm } from "react-hook-form";
import { Link, Prompt, useHistory } from "react-router-dom";
import { useAsync } from "react-use";

import { ACLRole } from "../apiclient/autogenerated";
import { schema, Schema } from "../components/acl/ACLFormSchema";
import { PageHeader } from "../components/common/PageHeader";
import { useTypedParams } from "../hooks/useTypedParams";

import {
  topPath,
  entityEntriesPath,
  entryDetailsPath,
  entitiesPath,
} from "Routes";
import { aironeApiClientV2 } from "apiclient/AironeApiClientV2";
import { ACLForm } from "components/common/ACLForm";
import { AironeBreadcrumbs } from "components/common/AironeBreadcrumbs";
import { Loading } from "components/common/Loading";
import { SubmitButton } from "components/common/SubmitButton";
import { DjangoContext } from "services/DjangoContext";

export const ACLPage: FC = () => {
  const djangoContext = DjangoContext.getInstance();
  const history = useHistory();
  const { enqueueSnackbar } = useSnackbar();
  const { objectId } = useTypedParams<{ objectId: number }>();

  const [submittable, setSubmittable] = useState<boolean>(false);
  const [aclInfo, _setACLInfo] = useState<{
    isPublic: boolean;
    permissions: Record<number, ACLRole>;
    defaultPermission?: number;
  }>({
    isPublic: false,
    permissions: {},
    defaultPermission: 1,
  });

  const [submitted, setSubmitted] = useState<boolean>(false);
  const [edited, setEdited] = useState<boolean>(false);

  const {
    formState: { isValid, isDirty, isSubmitting, isSubmitSuccessful },
    handleSubmit,
    reset,
    setError,
    setValue,
    control,
    getValues,
    watch,
  } = useForm<Schema>({
    resolver: zodResolver(schema),
    mode: "onBlur",
  });

  const acl = useAsync(async () => {
    return await aironeApiClientV2.getAcl(objectId);
  });

  /*
  const setACLInfo = (aclInfo: {
    isPublic: boolean;
    permissions: Record<number, ACLRole>;
    defaultPermission?: number;
  }) => {
    setEdited(true);
    _setACLInfo(aclInfo);
  };
  */

  const handleSubmitOnValid = useCallback(
    async (acl: Schema) => {
      console.log("[onix/handleSubmitOnValid(10)] acl: ", acl);
      // TODO better name?
      /*
      const aclSettings =
        acl.value?.roles.map((role) => ({
          member_id: role.id,
          value: aclInfo.permissions[role.id]?.currentPermission,
        })) ?? [];
      */
      await aironeApiClientV2.updateAcl(
        objectId,
        acl.isPublic,
        acl.roles,
        acl.objtype,
        acl.defaultPermission
      );

      enqueueSnackbar("ACL 設定の更新が成功しました", { variant: "success" });
      history.goBack();
    },
    [objectId]
  );

  const handleCancel = async () => {
    history.goBack();
  };

  /* initialize permissions and isPublic variables from acl parameter */
  useEffect(() => {
    if (!acl.loading && acl.value != null) {
      _setACLInfo({
        isPublic: acl.value.isPublic ?? false,
        permissions: acl.value.roles.reduce(
          (obj, role) => ({
            ...obj,
            [role.id]: role,
          }),
          {}
        ),
        defaultPermission: acl.value.defaultPermission,
      });
    }
    console.log("acl", acl);
    reset({
      isPublic: acl.value?.isPublic,
      defaultPermission: acl.value?.defaultPermission,
      objtype: acl.value?.objtype,
      roles: acl.value?.roles,
    });
  }, [acl]);

  return (
    <Box className="container-fluid">
      <AironeBreadcrumbs>
        <Typography component={Link} to={topPath()}>
          Top
        </Typography>
        <Typography component={Link} to={entitiesPath()}>
          エンティティ一覧
        </Typography>

        {!acl.loading && acl.value != null && (
          <>
            {/* This is a statement for Entity */}
            {((acl.value.objtype ?? 0) &
              (djangoContext?.aclObjectType.entity ?? 0)) >
              0 && (
                <Box sx={{ display: "flex" }}>
                  <Typography
                    component={Link}
                    to={entityEntriesPath(acl.value.id)}
                  >
                    {acl.value?.name}
                  </Typography>
                  {acl.value?.isPublic === false && <LockIcon />}
                </Box>
              )}

            {/* This is a statement for Entry */}
            {((acl.value.objtype ?? 0) &
              (djangoContext?.aclObjectType.entry ?? 0)) >
              0 && (
                <Box sx={{ display: "flex" }}>
                  <Typography
                    component={Link}
                    to={entityEntriesPath(acl.value.parent?.id ?? 0)}
                  >
                    {acl.value?.parent?.name}
                  </Typography>
                  {acl.value?.parent?.isPublic === false && <LockIcon />}
                </Box>
              )}
            {((acl.value.objtype ?? 0) &
              (djangoContext?.aclObjectType.entry ?? 0)) >
              0 && (
                <Box sx={{ display: "flex" }}>
                  <Typography
                    component={Link}
                    to={entryDetailsPath(acl.value.parent?.id ?? 0, acl.value.id)}
                  >
                    {acl.value?.name}
                  </Typography>
                  {acl.value?.isPublic === false && <LockIcon />}
                </Box>
              )}

            {/* This is a statement for EntityAttr */}
            {((acl.value.objtype ?? 0) &
              (djangoContext?.aclObjectType.entityAttr ?? 0)) >
              0 && (
                <Box sx={{ display: "flex" }}>
                  <Typography
                    component={Link}
                    to={entityEntriesPath(acl.value.parent?.id ?? 0)}
                  >
                    {acl.value?.parent?.name}
                  </Typography>
                  {acl.value?.parent?.isPublic === false && <LockIcon />}
                </Box>
              )}
            {((acl.value.objtype ?? 0) &
              (djangoContext?.aclObjectType.entityAttr ?? 0)) >
              0 && (
                <Box sx={{ display: "flex" }}>
                  <Typography color="textPrimary">{acl.value?.name}</Typography>
                  {acl.value?.isPublic === false && <LockIcon />}
                </Box>
              )}

            {/* This is a statement for EntryAttr */}
          </>
        )}
        <Typography color="textPrimary">ACL</Typography>
      </AironeBreadcrumbs>

      <PageHeader title={acl.value?.name ?? ""} description="ACL設定">
        <SubmitButton
          name="保存1"
          disabled={!submittable}
          handleSubmit={handleSubmit(handleSubmitOnValid)}
          handleCancel={handleCancel}
        />
      </PageHeader>

      {acl.loading ? (
        <Loading />
      ) : (
        <Container>
          <ACLForm
            control={control}
            getValues={getValues}
            watch={watch}
          />
        </Container>
      )}

      <Prompt
        when={edited && !submitted}
        message="編集した内容は失われてしまいますが、このページを離れてもよろしいですか？"
      />
    </Box>
  );
};
