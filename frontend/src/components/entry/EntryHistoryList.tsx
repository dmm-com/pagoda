import RestoreIcon from "@mui/icons-material/Restore";
import {
  Box,
  IconButton,
  Pagination,
  Stack,
  Table,
  TableBody,
  TableCell,
  TableHead,
  TableRow,
} from "@mui/material";
import { useSnackbar } from "notistack";
import React, { FC, useCallback } from "react";
import { useHistory } from "react-router-dom";

import { showEntryHistoryPath, topPath } from "../../Routes";
import { aironeApiClientV2 } from "../../apiclient/AironeApiClientV2";
import { EntryHistory } from "../../apiclient/autogenerated";
import { formatDate } from "../../services/DateUtil";
import { Confirmable } from "../common/Confirmable";

import { AttributeValue } from "./AttributeValue";

interface Props {
  histories: Array<EntryHistory>;
  entryId: number;
  page: number;
  maxPage: number;
  handleChangePage: (page: number) => void;
}

export const EntryHistoryList: FC<Props> = ({
  histories,
  entryId,
  page,
  maxPage,
  handleChangePage,
}) => {
  const { enqueueSnackbar } = useSnackbar();
  const history = useHistory();

  const handleRestore = useCallback(async (attrValueId: number) => {
    try {
      await aironeApiClientV2.restoreEntryHistory(attrValueId);
      enqueueSnackbar(`変更の復旧が完了しました`, {
        variant: "success",
      });
      history.replace(topPath());
      history.replace(showEntryHistoryPath(entryId));
    } catch (e) {
      enqueueSnackbar(`変更の復旧が失敗しました`, {
        variant: "error",
      });
    }
  }, []);

  return (
    <Box>
      <Table>
        <TableHead>
          <TableRow sx={{ backgroundColor: "#455A64" }}>
            <TableCell sx={{ color: "#FFFFFF" }}>項目</TableCell>
            <TableCell sx={{ color: "#FFFFFF" }}>変更前</TableCell>
            <TableCell sx={{ color: "#FFFFFF" }}>変更後</TableCell>
            <TableCell sx={{ color: "#FFFFFF" }}>実行日時</TableCell>
            <TableCell sx={{ color: "#FFFFFF" }}>実行者</TableCell>
            <TableCell sx={{ color: "#FFFFFF" }}>復旧</TableCell>
          </TableRow>
        </TableHead>

        <TableBody>
          {histories.map((history) => (
            <TableRow>
              <TableCell>{history.name}</TableCell>
              <TableCell>
                {history.prev != null && history.prev.value != null && (
                  <AttributeValue
                    attrInfo={{
                      type: history.prev.type,
                      value: history.prev.value,
                    }}
                  />
                )}
              </TableCell>
              <TableCell>
                {history.curr.value != null && (
                  <AttributeValue
                    attrInfo={{
                      type: history.curr.type,
                      value: history.curr.value,
                    }}
                  />
                )}
              </TableCell>
              <TableCell>{formatDate(history.curr.createdTime)}</TableCell>
              <TableCell>{history.curr.createdUser}</TableCell>
              <TableCell>
                {!history.curr.isLatest && (
                  <Confirmable
                    componentGenerator={(handleOpen) => (
                      <IconButton onClick={handleOpen}>
                        <RestoreIcon />
                      </IconButton>
                    )}
                    dialogTitle={`本当に復旧しますか？`}
                    onClickYes={() => handleRestore(history.curr.id)}
                  />
                )}
              </TableCell>
            </TableRow>
          ))}
        </TableBody>
      </Table>

      <Box display="flex" justifyContent="center" my="30px">
        <Stack spacing={2}>
          <Pagination
            count={maxPage}
            page={page}
            onChange={(e, page) => handleChangePage(page)}
            color="primary"
          />
        </Stack>
      </Box>
    </Box>
  );
};
